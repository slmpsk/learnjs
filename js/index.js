function describeAge(age,str) {
  str="You're a(n)"
  if (age <= 12) {
    return str+" kid";
  } else if (age >= 13 && age <= 17) {
    return str+" teenager";
  } else if (age >= 18 && age <= 64) {
    return str+" adult";
  } else {
    return str+" elderly";
  }
};


